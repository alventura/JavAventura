package com.class7;

public class CompoundTask {
	public static void main(String [] args) {
		int a=200;
		a+=100;
		System.out.println(a);
		
		a-=67;
		System.out.println(a);
		
		double cakePiece=11;
		
		cakePiece/=4;
		System.out.println(cakePiece);
		
		int cakePiece2;
		cakePiece2=25;
		
		cakePiece2/=7;
		System.out.println(cakePiece2);
		
		cakePiece2=25;
		cakePiece2%=7;
		System.out.println(cakePiece2);
				
		
		
	}
}
